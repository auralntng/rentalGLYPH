    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyy-MM-dd");
        String pinjam = dateFormat.format(Pinjam.getDate());
        String kembali = dateFormat.format(Kembali.getDate());
            
            if(Nama.getText().equals("") || 
                    Alamat.getText().equals("") || 
                    Motor.getSelectedItem().equals("") ||
                    Nopol.getText().equals("") || 
                    Nostruk.getText().equals("") ||
                    Harga.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error",
                        JOptionPane.WARNING_MESSAGE);
            } else {
                String SQL = "INSERT INTO peminjaman(nama, alamat, motor, no_pol, tgl_pinjam, tgl_kembali, harga)"+
                        "VALUES('"+Nama.getText()+"','"+Alamat.getText()+"',"+Motor.getSelectedItem().toString()+"',"+Nopol.getText()+"',"+Nostruk.getText()+"',"+pinjam+"',"+kembali+"'," +Harga.getText()+"')";
                int status = KoneksiDB.execute(SQL);
                if(status == 1){
                    JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan","SUKSES",
                            JOptionPane.INFORMATION_MESSAGE);
                    selectData();
                }else {
                    JOptionPane.showMessageDialog(this,"Data Gagal Ditambahkan","GAGAL",
                            JOptionPane.WARNING_MESSAGE);
                }
            }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if (baris != -1) {
            String NoStruk = tblData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM `peminjaman` WHERE no_struk='"+NoStruk+"'";
            int status = KoneksiDB.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal Dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }else {
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

