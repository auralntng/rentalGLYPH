    private void selectData() {
        String kolom[] = {"Nama","Alamat","Motor","NoPolisi","NoStruk","TanggalPinjam","TanggalKembali","Harga"};
                DefaultTableModel dtm = new DefaultTableModel(null, kolom);
                String SQL = "SELECT * FROM peminjaman";
                ResultSet rs = KoneksiDB.executeQuery(SQL);
                try{
                    while(rs.next()) {
                        String Nama = rs.getString(1);
                        String Alamat = rs.getString(2);
                        String Motor = rs.getString(3);
                        String NoPolisi = rs.getString(4);
                        String NoStruk = rs.getString(5);
                        String TanggalPinjam = rs.getString(6);
                        String TanggalKembali = rs.getString(7);
                        String Harga = rs.getString(8);
                        String data[] = {Nama,Alamat,Motor,NoPolisi,NoStruk,TanggalPinjam,TanggalKembali,Harga};
                        dtm.addRow(data);
                    }
                }    
                catch (SQLException ex) {
                    Logger.getLogger(pinjam.class.getName()).log(Level.SEVERE, null, ex);
                }
                tblData.setModel(dtm);
    }
